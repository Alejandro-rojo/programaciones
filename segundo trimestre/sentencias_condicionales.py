vocal=input("ingresa una vocal: ")
if vocal=="a":#convierte a mayuscula
    print("A")
elif vocal=="A":#convierte a minuscula
    print("a")
elif vocal=="e":#convierte a mayuscula
    print("E")
elif vocal=="E":#convierte a minuscula
    print("e")
elif vocal=="i":#convierte a mayuscula
    print("I")
elif vocal=="I":#convierte a minuscula
    print("i")
elif vocal=="o":#convierte a mayuscula
    print("O")
elif vocal=="O":#convierte a minuscula
    print("o")
elif vocal=="U":#convierte a minuscula
    print("u")
elif vocal=="u":#convierte a mayuscula
    print("U")
else:
    print(f"{vocal} no es una vocal")
